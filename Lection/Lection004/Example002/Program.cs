void Print(int [,] pic)
{
for ( int i = 0; i < pic.GetLength(0); i++)
{
    for ( int j = 0; j < pic.GetLength(1); j++)
    {
    
        if ( pic[i,j] == 0)
        {
            System.Console.Write($" ");
        }
        else
        {
            System.Console.Write($"+");
        }
    }
    System.Console.WriteLine();
}
}

void Fill(int i, int j)
{
    if ( pic[i,j] == 0)
    {
        pic[i,j] = 1;
        Fill(i-1,j);
        Fill(i,j-1);
        Fill(i+1,j);
        Fill(i,j+1);
    }
}

